							<p>What could be more fun than writing a story together with your friends? Well, writing an endless, collaborative choose-your-own-adventure story with your friends, obviously.</p>
							<p><strong>S.A.L.S.A Writer</strong> is meant to be just that. It was developed as an iPad app with hopes for a website version in the future, and would allow users to contribute to choice-based, branching stories created by other users (or to create their own personal stories). You can think of it as a combination forum&shy;/&shy;novel&shy;/&shy;RPG&shy;/&shy;application.</p>
							<p>The app, while pretty much feature-complete and working, won't be ready for release until the user management system is completed. And since our class split ways a few years ago, the application has been thrown into development limbo. Regardless, it was one of my favourite class projects, and there were a lot of features we hoped to implement in the future like points/ratings, user profiles, and private stories to name a few.</p>
							<p>My role in this project was (unexpectedly) focused on project management. I was responsible for delegating tasks, compiling the feature list, and finalizing the user interface map... all on top of the interface design and database interaction I was working on.</p>
